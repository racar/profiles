  def search
    @value = element.attributes[:value]
    query = UserIndex.search('*' + @value.to_s + '*')
    @total_results = query.total_entries
    users = query.records
    morph "#foo", ApplicationController.render(partial: "users/search_result", locals: {users: users})
  end
end
